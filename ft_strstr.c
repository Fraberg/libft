#include "header.h"

char	*ft_strstr(const char *m, const char *a)
{
	int i;
	int j;

	i = -1;
	while (m[++i] != '\0')
	{
		j = 0;
		while (a[j] == m[i + j])
			j++;
		if (a[j] == '\0')
			return (((char *)m) + i);
	}
	return ((char *)0);
}
