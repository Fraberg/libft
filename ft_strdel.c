#include "header.h"

void	ft_strdel(char **as)
{
	char	*ptr;
	int		i;

	if (as)
	{
		ptr = *as;
		i = -1;
		while (ptr[++i] != '\0')
			free(as[i]);
		free(as);
		as = NULL;
	}
}
